    string GetUserIdFromToken()
    {
        var claimsIdentity = HttpContext.User.Identity as ClaimsIdentity;
        if (claimsIdentity != null)
            return claimsIdentity.Claims.First(claim => claim.Type == "Id").Value;
        return "";
    }

